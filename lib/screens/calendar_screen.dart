import 'package:flutter/material.dart';
import 'package:table_calendar/table_calendar.dart';
import 'package:provider/provider.dart';
import 'package:recipe_manager/providers/theme_provider.dart';

import '../providers/calendar_provider.dart';
import '../models/calendar_model.dart';
import '../models/recipe_model.dart';

class CalendarScreen extends StatefulWidget {
  const CalendarScreen({super.key});

  @override
  State<CalendarScreen> createState() => _CalendarScreenState();
}

class _CalendarScreenState extends State<CalendarScreen> {
  CalendarFormat _calendarFormat = CalendarFormat.month;
  DateTime _focusedDay = DateTime.now();
  DateTime? _selectedDay;

  // State: selected category and selected recipe for each of the 5 rows
  final List<String?> _selectedCategories = List<String?>.filled(5, null);
  final List<Recipe?> _selectedRecipes = List<Recipe?>.filled(5, null);

  @override
  void initState() {
    super.initState();

    // Load necessary data from Supabase upon screen initialization
    WidgetsBinding.instance.addPostFrameCallback((_) {
      if (!mounted) return;

      final mealProvider = context.read<MealDayProvider>();
      // Meals already assigned to specific days
      mealProvider.fetchMeals();
      // Recipes available to choose by meal type
      mealProvider.fetchCalendarRecipes();
    });
  }

  // Handles saving a meal using the provider and the MealDay model
  Future<bool> _onAddMeal({
    required String mealCategory,
    required Recipe selectedRecipe,
  }) async {
    if (_selectedDay == null) {
      debugPrint('Error: No day selected for meal saving.');
      return false;
    }

    final provider = context.read<MealDayProvider>();

    final meal = MealDay(
      idMeal: '', // Generated by Supabase
      createdAt: DateTime.now(),
      userId: '', // Overwritten in the provider with the current user
      mealCategory: mealCategory,
      // We save the selected recipe name to display it later
      ingredients: selectedRecipe.title,
      eatDate: DateTime(
        _selectedDay!.year,
        _selectedDay!.month,
        _selectedDay!.day,
      ),
    );

    // addMeal returns true/false depending on success
    return await provider.addMeal(meal);
  }

  // === FUNCTION: Save all selected meals at once ===
  Future<void> _onSaveAllMeals() async {
    if (_selectedDay == null) return;

    // Use a local BuildContext to show the SnackBar after an await
    final localContext = context;
    int mealsSavedCount = 0;

    // Iterate over the 5 selection rows
    for (var i = 0; i < _selectedCategories.length; i++) {
      final selectedCategory = _selectedCategories[i];
      final selectedRecipe = _selectedRecipes[i];

      // Check if the row has a valid selection
      if (selectedCategory != null && selectedRecipe != null) {
        final success = await _onAddMeal(
          mealCategory: selectedCategory,
          selectedRecipe: selectedRecipe,
        );
        if (success) {
          mealsSavedCount++;
        }
      }
    }

    // Close the dialog before showing the message (to avoid context issues)
    if (localContext.mounted) {
      Navigator.pop(localContext);
    }

    if (localContext.mounted) {
      ScaffoldMessenger.of(localContext).showSnackBar(
        SnackBar(
          content: Text(
            mealsSavedCount > 0
                ? '$mealsSavedCount meal(s) saved successfully.'
                : 'No selected meals to save.',
          ),
          duration: const Duration(seconds: 3),
        ),
      );
    }
  }

  @override
  Widget build(BuildContext context) {
    final themeProvider = Provider.of<ThemeProvider>(context);
    final isDark = themeProvider.isDarkMode;

    return Scaffold(
      appBar: AppBar(
        title: const Text('Calendar'),
      ),
      body: Container(
        color: const Color(0xFFEEE0CB),
        child: Column(
          children: [
            TableCalendar(
              firstDay: DateTime.utc(2010, 10, 16),
              lastDay: DateTime.utc(2030, 3, 14),
              focusedDay: _focusedDay,
              calendarFormat: _calendarFormat,
              selectedDayPredicate: (day) => isSameDay(_selectedDay, day),
              onDaySelected: (selectedDay, focusedDay) {
                if (!isSameDay(_selectedDay, selectedDay)) {
                  setState(() {
                    _selectedDay = DateTime(
                      selectedDay.year,
                      selectedDay.month,
                      selectedDay.day,
                    );
                    _focusedDay = focusedDay;

                    // Reset category/meal selections every time the popup opens
                    for (var i = 0; i < _selectedCategories.length; i++) {
                      _selectedCategories[i] = null;
                      _selectedRecipes[i] = null;
                    }
                  });

                  Future.delayed(
                    const Duration(milliseconds: 300),
                    () {
                      if (!context.mounted) return;
                      showDialog(
                        context: context,
                        builder: (context) {
                          return StatefulBuilder(
                            builder: (BuildContext dialogContext,
                                StateSetter dialogSetState) {
                              return Dialog(
                                shape: RoundedRectangleBorder(
                                  borderRadius: BorderRadius.circular(16),
                                ),
                                child: Container(
                                  padding: const EdgeInsets.all(20),
                                  decoration: BoxDecoration(
                                    color: const Color(0xFFEEE0CB),
                                    borderRadius: BorderRadius.circular(16),
                                  ),
                                  child: Column(
                                    mainAxisSize: MainAxisSize.min,
                                    children: [
                                      const Text(
                                        'Your meals for today',
                                        style: TextStyle(
                                          fontSize: 18,
                                          fontWeight: FontWeight.bold,
                                        ),
                                      ),
                                      const SizedBox(height: 8),
                                      Text(
                                        '${selectedDay.day}/${selectedDay.month}/${selectedDay.year}',
                                        style: const TextStyle(fontSize: 16),
                                      ),
                                      const SizedBox(height: 12),

                                      // ===== Dropdowns (5 rows) =====
                                      Consumer<MealDayProvider>(
                                        builder: (context, mealProvider, _) {
                                          if (mealProvider.isLoadingRecipes &&
                                              mealProvider.recipes.isEmpty) {
                                            return const Padding(
                                              padding: EdgeInsets.all(16.0),
                                              child:
                                                  CircularProgressIndicator(),
                                            );
                                          }

                                          final categories =
                                              mealProvider.availableMealTypes;

                                          return Column(
                                            mainAxisSize: MainAxisSize.min,
                                            children: List.generate(5, (index) {
                                              return Padding(
                                                padding: const EdgeInsets.only(
                                                    bottom: 12),
                                                child: _buildMealRow(
                                                  rowIndex: index,
                                                  categories: categories,
                                                  mealProvider: mealProvider,
                                                  dialogSetState:
                                                      dialogSetState,
                                                ),
                                              );
                                            }),
                                          );
                                        },
                                      ),
                                      const SizedBox(height: 20),

                                      // ===== Save and Cancel Buttons =====
                                      Row(
                                        mainAxisAlignment:
                                            MainAxisAlignment.end,
                                        children: [
                                          TextButton(
                                            onPressed: () =>
                                                Navigator.pop(dialogContext),
                                            child: const Text('Cancel'),
                                          ),
                                          const SizedBox(width: 10),
                                          ElevatedButton(
                                            onPressed: _onSaveAllMeals,
                                            style: ElevatedButton.styleFrom(
                                              backgroundColor:
                                                  const Color(0xFF839788),
                                            ),
                                            child: const Text('Save Meals'),
                                          ),
                                        ],
                                      ),
                                    ],
                                  ),
                                ),
                              );
                            },
                          );
                        },
                      );
                    },
                  );
                }
              },
              onFormatChanged: (format) {
                if (_calendarFormat != format) {
                  setState(() => _calendarFormat = format);
                }
              },
              onPageChanged: (focusedDay) => _focusedDay = focusedDay,
              
              // === FIXED: All styles moved inside CalendarStyle ===
              calendarStyle: CalendarStyle(
                todayDecoration: const BoxDecoration(
                  color: Color(0xFF839788),
                  shape: BoxShape.circle,
                ),
                // Moved from outside to here:
                selectedDecoration: const BoxDecoration(
                  color: Color(0xFFBAA898),
                  shape: BoxShape.circle,
                ),
                // Moved from outside to here:
                defaultTextStyle: TextStyle(
                  color: isDark ? Colors.white : Colors.black,
                ),
                // Moved from outside to here:
                weekendTextStyle: TextStyle(
                  color: isDark ? Colors.white70 : Colors.black87,
                ),
                // Moved from outside to here:
                outsideTextStyle: TextStyle(
                  color: isDark ? Colors.grey[600] : Colors.grey[400],
                ),
              ),

              // === FIXED: These are now correctly inside TableCalendar ===
              headerStyle: HeaderStyle(
                formatButtonVisible: false,
                titleCentered: true,
                titleTextStyle: TextStyle(
                  color: isDark ? Colors.white : Colors.black,
                  fontSize: 17,
                  fontWeight: FontWeight.bold,
                ),
                leftChevronIcon: Icon(
                  Icons.chevron_left,
                  color: isDark ? Colors.white : Colors.black,
                ),
                rightChevronIcon: Icon(
                  Icons.chevron_right,
                  color: isDark ? Colors.white : Colors.black,
                ),
              ),
              daysOfWeekStyle: DaysOfWeekStyle(
                weekdayStyle: TextStyle(
                  color: isDark ? Colors.white70 : Colors.black87,
                ),
                weekendStyle: TextStyle(
                  color: isDark ? Colors.white70 : Colors.black87,
                ),
              ),
              calendarBuilders: CalendarBuilders(
                defaultBuilder: (context, day, focusedDay) {
                  return Container(
                    margin: const EdgeInsets.all(4.0),
                    alignment: Alignment.center,
                    child: Text(
                      '${day.day}',
                      style: TextStyle(
                        color: isDark ? Colors.white : Colors.black,
                      ),
                    ),
                  );
                },
              ),
            ),
            const SizedBox(height: 8.0),
            Expanded(
              child: Center(
                child: _selectedDay != null
                    ? Text(
                        'Selected day: ${_selectedDay!.year}-${_selectedDay!.month.toString().padLeft(2, '0')}-${_selectedDay!.day.toString().padLeft(2, '0')}',
                        style: Theme.of(context).textTheme.bodyLarge,
                      )
                    : Text(
                        'No day selected',
                        style: Theme.of(context).textTheme.bodyLarge,
                      ),
              ),
            ),
          ],
        ),
      ),
    );
  }

  /// meal row builder function
  Widget _buildMealRow({
    required int rowIndex,
    required List<String> categories,
    required MealDayProvider mealProvider,
    required StateSetter dialogSetState,
  }) {
    final String? selectedCategory = _selectedCategories[rowIndex];
    final Recipe? selectedRecipe = _selectedRecipes[rowIndex];

    final List<Recipe> recipesForCategory = selectedCategory == null
        ? <Recipe>[]
        : mealProvider.recipesForMealType(selectedCategory);

    return Row(
      children: [
        // Category Dropdown
        Expanded(
          flex: 1,
          child: DropdownButtonFormField<String>(
            initialValue: selectedCategory,
            hint: const Text('Category'),
            isExpanded: true,
            decoration: InputDecoration(
              contentPadding:
                  const EdgeInsets.symmetric(horizontal: 10, vertical: 8),
              filled: true,
              fillColor: Colors.grey[200],
              border: OutlineInputBorder(
                borderRadius: BorderRadius.circular(8),
                borderSide: BorderSide.none,
              ),
            ),
            items: categories
                .map(
                  (cat) => DropdownMenuItem<String>(
                    value: cat,
                    child: Text(cat),
                  ),
                )
                .toList(),
            onChanged: (value) {
              dialogSetState(() {
                _selectedCategories[rowIndex] = value;
                _selectedRecipes[rowIndex] =
                    null; // reset meal when changing category
              });
            },
          ),
        ),
        const SizedBox(width: 8),

        // Meal Dropdown filtered by category
        Expanded(
          flex: 2,
          child: DropdownButtonFormField<Recipe>(
            // If the previously selected recipe is no longer in the new category, its value is null
            initialValue: recipesForCategory.contains(selectedRecipe)
                ? selectedRecipe
                : null,
            hint: const Text('Meal'),
            isExpanded: true,
            decoration: InputDecoration(
              contentPadding:
                  const EdgeInsets.symmetric(horizontal: 10, vertical: 8),
              filled: true,
              fillColor: Colors.grey[200],
              border: OutlineInputBorder(
                borderRadius: BorderRadius.circular(8),
                borderSide: BorderSide.none,
              ),
            ),
            items: recipesForCategory
                .map(
                  (recipe) => DropdownMenuItem<Recipe>(
                    value: recipe,
                    child: Text(recipe.title),
                  ),
                )
                .toList(),
            onChanged: (recipe) {
              dialogSetState(() {
                _selectedRecipes[rowIndex] = recipe;
              });
            },
          ),
        ),
      ],
    );
  }
}